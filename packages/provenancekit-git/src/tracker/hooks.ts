/**
 * Template for pre-push hook.
 */
function generatePrePushScript(config: GitHookConfig): string {
  const { storageUrl, contractAddress, chainId, envVars = {} } = config;

  const envLines = Object.entries(envVars)
    .map(([key, value]) => `export ${key}="${value}"`)
    .join("\n");

  return `#!/bin/bash
# ProvenanceKit pre-push hook
# Generated by @provenancekit/git
# This hook records provenance before pushing

set -e

${envLines}

REMOTE=$1
URL=$2

# Read the pushed commits
while read local_ref local_sha remote_ref remote_sha; do
  if [ "$local_sha" = "0000000000000000000000000000000000000000" ]; then
    # Branch deletion, skip
    continue
  fi

  if [ "$remote_sha" = "0000000000000000000000000000000000000000" ]; then
    # New branch, record all commits
    RANGE="$local_sha"
  else
    # Existing branch, record new commits
    RANGE="$remote_sha..$local_sha"
  fi

  echo "Recording provenance for commits: $RANGE"

  # Record each commit in the range
  for COMMIT in $(git rev-list --reverse $RANGE); do
    npx @provenancekit/git record-commit \\
      --commit "$COMMIT" \\
      ${storageUrl ? `--storage-url "${storageUrl}"` : ""} \\
      ${contractAddress ? `--contract "${contractAddress}"` : ""} \\
      ${chainId ? `--chain-id ${chainId}` : ""}
  done
done

exit 0
`;
}

