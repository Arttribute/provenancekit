/**
 * Git hooks generator for automatic provenance recording.
 *
 * Generates git hooks that automatically record provenance
 * for commits, pushes, and other git operations.
 *
 * @packageDocumentation
 */

import * as fs from "fs";
import * as path from "path";
import type { GitHookType, GitHookConfig, GeneratedHook } from "../types.js";
import { GitError } from "../types.js";

