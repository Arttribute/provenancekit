/**
 * Generate a git hook script.
 *
 * @param config - Hook configuration
 * @returns Generated hook with script content
 *
 * @example
 * ```typescript
 * const hook = generateHook({
 *   hookType: "post-commit",
 *   storageUrl: "https://api.provenancekit.org/storage",
 *   aiDetection: true,
 * });
 *
 * console.log(hook.script);
 * ```
 */
export function generateHook(config: GitHookConfig): GeneratedHook {
  let script: string;

  switch (config.hookType) {
    case "post-commit":
      script = generatePostCommitScript(config);
      break;
    case "prepare-commit-msg":
      script = generatePrepareCommitMsgScript(config);
      break;
    case "commit-msg":
      script = generateCommitMsgScript(config);
      break;
    case "pre-commit":
      script = generatePreCommitScript(config);
      break;
    case "pre-push":
      script = generatePrePushScript(config);
      break;
    default:
      throw new GitError(
        `Unsupported hook type: ${config.hookType}`,
        "INVALID_OPTIONS",
        { hookType: config.hookType }
      );
  }

  return {
    hookType: config.hookType,
    script,
    installPath: `.git/hooks/${config.hookType}`,
  };
}

/**
 * Install a git hook in a repository.
 *
 * @param repoPath - Path to the repository
 * @param hook - Generated hook to install
 * @param options - Installation options
 *
 * @example
 * ```typescript
 * const hook = generateHook({ hookType: "post-commit" });
 * await installHook("/path/to/repo", hook);
 * ```
 */
export async function installHook(
  repoPath: string,
  hook: GeneratedHook,
  options: { backup?: boolean; force?: boolean } = {}
): Promise<void> {
  const { backup = true, force = false } = options;

  const hookPath = path.join(repoPath, hook.installPath);
  const hooksDir = path.dirname(hookPath);

  // Ensure hooks directory exists
  if (!fs.existsSync(hooksDir)) {
    fs.mkdirSync(hooksDir, { recursive: true });
  }

  // Check if hook already exists
  if (fs.existsSync(hookPath)) {
    if (!force) {
      throw new GitError(
        `Hook already exists: ${hook.hookType}. Use force option to overwrite.`,
        "INVALID_OPTIONS",
        { hookType: hook.hookType, path: hookPath }
      );
    }

    // Backup existing hook
    if (backup) {
      const backupPath = `${hookPath}.backup.${Date.now()}`;
      fs.copyFileSync(hookPath, backupPath);
    }
  }

  // Write the hook
  fs.writeFileSync(hookPath, hook.script, { mode: 0o755 });
}

/**
 * Uninstall a git hook from a repository.
 *
 * @param repoPath - Path to the repository
 * @param hookType - Type of hook to uninstall
 * @param options - Uninstallation options
 */
export async function uninstallHook(
  repoPath: string,
  hookType: GitHookType,
  options: { restoreBackup?: boolean } = {}
): Promise<void> {
  const { restoreBackup = false } = options;

  const hookPath = path.join(repoPath, ".git/hooks", hookType);

  if (!fs.existsSync(hookPath)) {
    return; // Hook doesn't exist, nothing to do
  }

  // Remove the hook
  fs.unlinkSync(hookPath);

  // Optionally restore backup
  if (restoreBackup) {
    const backupFiles = fs
      .readdirSync(path.dirname(hookPath))
      .filter((f) => f.startsWith(`${hookType}.backup.`))
      .sort()
      .reverse();

    if (backupFiles.length > 0) {
      const latestBackup = path.join(path.dirname(hookPath), backupFiles[0]);
      fs.copyFileSync(latestBackup, hookPath);
      fs.chmodSync(hookPath, 0o755);
    }
  }
}

/**
 * Initialize ProvenanceKit hooks in a repository.
 *
 * Installs all recommended hooks for provenance tracking.
 *
 * @param repoPath - Path to the repository
 * @param config - Base configuration for hooks
 * @returns Array of installed hooks
 *
 * @example
 * ```typescript
 * const installed = await initializeHooks("/path/to/repo", {
 *   storageUrl: "https://api.provenancekit.org/storage",
 *   aiDetection: true,
 * });
 *
 * console.log(`Installed ${installed.length} hooks`);
 * ```
 */
export async function initializeHooks(
  repoPath: string,
  config: Omit<GitHookConfig, "hookType"> = {}
): Promise<GeneratedHook[]> {
  const hookTypes: GitHookType[] = [
    "prepare-commit-msg",
    "post-commit",
  ];

  const installed: GeneratedHook[] = [];

  for (const hookType of hookTypes) {
    const hook = generateHook({ ...config, hookType });
    await installHook(repoPath, hook, { force: true, backup: true });
    installed.push(hook);
  }

  return installed;
}

/**
 * Check if ProvenanceKit hooks are installed in a repository.
 *
 * @param repoPath - Path to the repository
 * @returns Object with hook installation status
 */
export function checkHooksInstalled(
  repoPath: string
): Record<GitHookType, boolean> {
  const hookTypes: GitHookType[] = [
    "pre-commit",
    "prepare-commit-msg",
    "commit-msg",
    "post-commit",
    "pre-push",
  ];

  const status: Record<string, boolean> = {};

  for (const hookType of hookTypes) {
    const hookPath = path.join(repoPath, ".git/hooks", hookType);

    if (fs.existsSync(hookPath)) {
      // Check if it's a ProvenanceKit hook
      const content = fs.readFileSync(hookPath, "utf-8");
      status[hookType] = content.includes("ProvenanceKit");
    } else {
      status[hookType] = false;
    }
  }

  return status as Record<GitHookType, boolean>;
}
