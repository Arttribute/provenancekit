/**
 * Template for post-commit hook.
 */
function generatePostCommitScript(config: GitHookConfig): string {
  const { storageUrl, contractAddress, chainId, aiDetection = true, postScript, envVars = {} } = config;

  const envLines = Object.entries(envVars)
    .map(([key, value]) => `export ${key}="${value}"`)
    .join("\n");

  return `#!/bin/bash
# ProvenanceKit post-commit hook
# Generated by @provenancekit/git
# This hook records provenance for each commit

set -e

${envLines}

# Get the commit hash
COMMIT_SHA=$(git rev-parse HEAD)
REPO_PATH=$(git rev-parse --show-toplevel)

echo "Recording provenance for commit $COMMIT_SHA..."

# Run the provenance recording
npx @provenancekit/git record-commit \\
  --commit "$COMMIT_SHA" \\
  --repo "$REPO_PATH" \\
  ${aiDetection ? "--detect-ai" : ""} \\
  ${storageUrl ? `--storage-url "${storageUrl}"` : ""} \\
  ${contractAddress ? `--contract "${contractAddress}"` : ""} \\
  ${chainId ? `--chain-id ${chainId}` : ""}

${postScript ?? ""}

echo "Provenance recorded successfully."
`;
}

/**
 * Template for prepare-commit-msg hook.
 * Adds AI co-author line if AI assistance is detected.
 */
function generatePrepareCommitMsgScript(config: GitHookConfig): string {
  const { envVars = {} } = config;

  const envLines = Object.entries(envVars)
    .map(([key, value]) => `export ${key}="${value}"`)
    .join("\n");

  return `#!/bin/bash
# ProvenanceKit prepare-commit-msg hook
# Generated by @provenancekit/git
# This hook adds AI co-author information if detected

${envLines}

COMMIT_MSG_FILE=$1
COMMIT_SOURCE=$2

# Only modify for regular commits, not merges, squashes, etc.
if [ -n "$COMMIT_SOURCE" ] && [ "$COMMIT_SOURCE" != "message" ]; then
  exit 0
fi

# Check for AI tool indicators in the environment or session
AI_TOOL=""

# Check for Cursor
if [ -n "$CURSOR_SESSION" ] || [ -d ".cursor" ]; then
  AI_TOOL="Cursor"
fi

# Check for GitHub Copilot
if [ -n "$GITHUB_COPILOT_ENABLED" ]; then
  AI_TOOL="GitHub Copilot"
fi

# Check for Aider
if [ -n "$AIDER_SESSION" ] || [ -f ".aider" ]; then
  AI_TOOL="Aider"
fi

# Add co-author line if AI tool detected and not already present
if [ -n "$AI_TOOL" ]; then
  if ! grep -q "Co-authored-by:.*$AI_TOOL" "$COMMIT_MSG_FILE"; then
    echo "" >> "$COMMIT_MSG_FILE"
    echo "Co-authored-by: $AI_TOOL <ai-assistant@provenancekit.org>" >> "$COMMIT_MSG_FILE"
  fi
fi
`;
}

